package com.sixnine.live.bean;

public class AdvertiseInfo {

	private String id;
	private String focusTitle;
	private String focusLinkUrl;
	private String focusPicUrl;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFocusTitle() {
		return focusTitle;
	}
	public void setFocusTitle(String focusTitle) {
		this.focusTitle = focusTitle;
	}
	public String getFocusLinkUrl() {
		return focusLinkUrl;
	}
	public void setFocusLinkUrl(String focusLinkUrl) {
		this.focusLinkUrl = focusLinkUrl;
	}
	public String getFocusPicUrl() {
		return focusPicUrl;
	}
	public void setFocusPicUrl(String focusPicUrl) {
		this.focusPicUrl = focusPicUrl;
	}
	
}
